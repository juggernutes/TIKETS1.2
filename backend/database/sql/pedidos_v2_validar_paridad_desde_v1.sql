PRINT 'Resumen de unidades operacionales V1 pendientes de mapeo';
SELECT
    tu.Nombre AS TipoUnidadV1,
    s.Nombre AS SucursalV1,
    usr.Email AS EmailUsuarioV1,
    COUNT(*) AS UnidadesPendientes,
    CASE
      WHEN cu.ID_Usuario IS NULL THEN 'NO_INSERTAR: usuario V1 no existe en core.usuario V2 por Email'
      ELSE 'USUARIO_EXISTE: revisar supervisor antes de insertar'
    END AS Diagnostico
FROM #v1_unidad u
LEFT JOIN #v1_tipounidad tu ON tu.IdTipoUnidad=u.IdTipoUnidad
LEFT JOIN core.sucursal s ON s.ID_Sucursal=u.IdSucursal
LEFT JOIN #v1_usuario usr ON usr.ID_Usuario=u.IdUsuario
LEFT JOIN core.usuario cu ON UPPER(cu.Email)=UPPER(usr.Email)
LEFT JOIN ped.unidadoperacional u2 ON UPPER(u2.Nombre)=UPPER(u.Nombre)
WHERE u2.IdUnidad IS NULL
GROUP BY tu.Nombre, s.Nombre, usr.Email, CASE WHEN cu.ID_Usuario IS NULL THEN 'NO_INSERTAR: usuario V1 no existe en core.usuario V2 por Email' ELSE 'USUARIO_EXISTE: revisar supervisor antes de insertar' END
ORDER BY Diagnostico, TipoUnidadV1, SucursalV1, EmailUsuarioV1;

PRINT 'Detalle opcional de unidades operacionales V1 pendientes de mapeo';
SELECT TOP 200
    u.IdUnidad AS IdUnidadV1,
    u.Nombre AS UnidadV1,
    tu.Nombre AS TipoUnidadV1,
    s.Nombre AS SucursalV1,
    usr.Email AS EmailUsuarioV1,
    u.IdSupervisor AS IdSupervisorV1,
    su.Nombre AS SupervisorV1,
    'PENDIENTE_MAPEO_USUARIO' AS Diagnostico
FROM #v1_unidad u
LEFT JOIN #v1_tipounidad tu ON tu.IdTipoUnidad=u.IdTipoUnidad
LEFT JOIN core.sucursal s ON s.ID_Sucursal=u.IdSucursal
LEFT JOIN #v1_usuario usr ON usr.ID_Usuario=u.IdUsuario
LEFT JOIN ped.unidadoperacional u2 ON UPPER(u2.Nombre)=UPPER(u.Nombre)
LEFT JOIN #v1_unidad su ON su.IdUnidad=u.IdSupervisor
WHERE u2.IdUnidad IS NULL
ORDER BY tu.Nombre, s.Nombre, u.IdUnidad;
